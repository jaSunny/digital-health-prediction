-- This SQL files converts all giving features from
-- tuk.PERSONS_ML into feature tables with INTs
--
--
-- Converting all Birthdates to INT
DROP TABLE BIRTH_DATE_TO_INT;
CREATE COLUMN TABLE BIRTH_DATE_TO_INT (
ID INTEGER PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
CLASSV NVARCHAR(255)
);
--
INSERT INTO  BIRTH_DATE_TO_INT
(select
"BIRTH_DATE"
 from tuk.PERSONS_ML2
 group by "BIRTH_DATE");
